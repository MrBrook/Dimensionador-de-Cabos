package br.com.database.dto;

public class ResistenciaReatanciaCabosDTO {

    private int idCabo;
    private float rcc;
    private float rca;
    private float xc;

    public int getIdCabo() {
        return idCabo;
    }

    public void setIdCabo(int idCabo) {
        this.idCabo = idCabo;
    }

    public float getRcc() {
        return rcc;
    }

    public void setRcc(float rcc) {
        this.rcc = rcc;
    }

    public float getRca() {
        return rca;
    }

    public void setRca(float rca) {
        this.rca = rca;
    }

    public float getXc() {
        return xc;
    }

    public void setXc(float xc) {
        this.xc = xc;
    }
}
